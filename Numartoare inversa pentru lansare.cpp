// Numartoare inversa pentru lansare.cpp : This file contains the 'main' function. Program execution begins and ends there.
//

#include <iostream>

int main() {
    // Numărătoarea inversă de la 10 la 1
    for (int i = 10; i >= 1; --i) {
        std::cout << i << std::endl;
    }

    // Afișează mesajul "Lansare!"
    std::cout << "Lansare!" << std::endl;

    return 0;
}